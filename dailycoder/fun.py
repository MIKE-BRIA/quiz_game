import turtle

turtle = turtle.Turtle()

def square():
  turtle.forward(80)
  turtle.left(90)
  turtle.forward(80)
  turtle.left(90)
  turtle.forward(80)
  turtle.left(90)
  turtle.forward(80)
while True:
  square()
  turtle.forward(90)
  square()
  turtle.forward(90)
  square()
  turtle.forward(90)
  square()
  turtle.forward(90)
  square()